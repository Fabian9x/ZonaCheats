// Función para cargar un componente específico
function loadComponent(element, componentName) {
    if (!element || !componentName) return;
    
    if (components[componentName]) {
        element.innerHTML = components[componentName];
        console.log(`✅ Component ${componentName} loaded successfully`);
    } else {
        console.error(`❌ Component ${componentName} not found`);
        element.innerHTML = `<p style="color: red; padding: 20px;">Component ${componentName} not found</p>`;
    }
}

// Función principal para inicializar componentes
async function initializeComponents(config) {
    if (!config || !config.components) {
        console.error('❌ No components configuration provided');
        return;
    }
    
    for (const componentConfig of config.components) {
        const elements = document.querySelectorAll(componentConfig.selector);
        elements.forEach(element => {
            loadComponent(element, componentConfig.name);
        });
    }
    
    console.log('🚀 All components initialized successfully');
}

// Auto-inicialización cuando se carga el DOM
document.addEventListener('DOMContentLoaded', function() {
    // Buscar elementos con data-component y cargarlos automáticamente
    const navElements = document.querySelectorAll('[data-component="nav"]');
    const footerElements = document.querySelectorAll('[data-component="footer"]');
    
    navElements.forEach(element => {
        loadComponent(element, 'nav');
    });
    
    footerElements.forEach(element => {
        loadComponent(element, 'footer');
    });
    
    console.log('🚀 Component loader initialized');
});
